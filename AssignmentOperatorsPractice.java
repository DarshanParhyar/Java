public class AssignmentOperatorsPractice {
    public static void main(String[] args) {
        int x = 10;
        x += 5;
        System.out.println("After x+: " + x);
        x -= 1;
        System.out.println("After x-: " + x);
        x *= 3;
        System.out.println("After x*: " + x);
        x /= 2;
        System.out.println("After x/: " + x);

    }
}
